import math

#Foctions mathématiques intégrées
x = min(5, 11, 35)
y = max(5, 10, 25)
print(x)
print(y)

#la valeur positive
x = abs(-7.25)
print(x)

#calcul des exponentiels
x = pow(4, 3)
print(x)

#la racine carrée
x = math.sqrt(64)
print(x)

#Arrondissement des nbres

x = math.ceil(3.4)
y = math.floor(2.5)
print(x)
print(y)

#La valeur de pi

x = math.pi
print(x)